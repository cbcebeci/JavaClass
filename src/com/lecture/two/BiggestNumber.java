package com.lecture.two;

public class BiggestNumber {

    public static void main(String[] args) {

        int firstnumber = 5;
        int secondnumber = 12;

        if (firstnumber > secondnumber) {
            System.out.println(firstnumber);
        }

        if (secondnumber > firstnumber) {
            System.out.println(secondnumber);
        }
    }
}
